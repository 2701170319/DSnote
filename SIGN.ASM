DATA SEGMENT
  BLOCK DB 03H,45H,90H
  CNT EQU $-BLOCK
  PB DB CNT DUP(?)
  MB DB CNT DUP(?)
DATA ENDS

CODE SEGMENT
  ASSUME DS:DATA,CS:CODE
START: MOV AX,DATA
       MOV DS,AX
      LEA BX,BLOCK
      LEA SI,PB
      LEA DI,MB
      MOV CX,CNT
LP:   MOV AL,[BX]
      TEST AL,80H
      JZ ZH
      MOV [DI],AL
      INC DI
      JMP A1 
ZH:   MOV [SI],AL
      INC SI
A1:   INC BX
LOOP LP
MOV AH,4CH
INT 21H
CODE ENDS
END START
